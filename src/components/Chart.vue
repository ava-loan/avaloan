<script>
  import Chart from 'chart.js'
  import { generateChart } from 'vue-chartjs'

  Chart.defaults.LineWithLine = Chart.defaults.line;
  Chart.controllers.LineWithLine = Chart.controllers.line.extend({
    draw: function(ease) {
      Chart.controllers.line.prototype.draw.call(this, ease);

      if (this.chart.tooltip._active && this.chart.tooltip._active.length) {
        var activePoint = this.chart.tooltip._active[0],
        ctx = this.chart.ctx,
        x = activePoint.tooltipPosition().x,
        y = activePoint.tooltipPosition().y,
        topY = this.chart.scales['y-axis-0'].top,
        bottomY = this.chart.scales['y-axis-0'].bottom;

         // draw line
         ctx.save();
         ctx.beginPath();
         ctx.moveTo(x, topY);
         ctx.lineTo(x, bottomY);
         ctx.lineWidth = 1;
         ctx.strokeStyle = '#b9b7ff';
         ctx.stroke();

        // draw point
        ctx.beginPath();
        ctx.arc(x, y, 4, 0, Math.PI * 2);
        ctx.strokeStyle = '#6b70ed';
        ctx.lineWidth = 2;
        ctx.stroke();
        ctx.closePath();
        ctx.fill();

        ctx.restore();
      }
   }
  })

  const CustomLine = generateChart('custom-line', 'LineWithLine')

  export default {
    name: 'Chart',
    extends: CustomLine,
    props: {
      dataPoints: {
        type: Array,
        default: () => [],
      },
      minY: 0,
      maxY: null,
      height: null,
      width: null,
      lineWidth: null,
      stepped: false
    },
    data() {
      return {
      }
    },
    mounted() {
      this.renderChart(this.chartData, this.options)
    },
    computed: {
      minX() {
        return this.dataPoints[0].x;
      },
      maxX() {
        return this.dataPoints.slice(-1)[0].x;
      },
      chartData() {
        return {
          labels: ['a', 'b', 'c'],
          datasets: [
            {
              fill: false,
              steppedLine: this.stepped,
              data: this.dataPoints,
              borderColor: (context) => this.getColor(context, this.getLineGradient),
              borderWidth: this.lineWidth
            }
          ]
        };
      },
      options() {
        return {
          aspectRatio: this.isMobile ? 2 : 4,
          height: null,
          width: null,
          legend: {
            display: false
          },
          elements: {
            point:{
              radius: 0
            }
          },
          scales: {
            xAxes: [{
              type: 'time',
              gridLines: {
                drawOnChartArea: false,
                tickMarkLength: 0,
                lineWidth: 0,
              },
              ticks: {
                beginAtZero: false,
                min: this.minX - (this.maxX - this.minX) / 50,
                max: this.maxX + (this.maxX - this.minX) / 50,
                maxTicksLimit: 1,
                maxRotation: 0,
                minRotation: 0,
                align: 'center',
                padding: 15,
                fontFamily: 'Montserrat'
              }
            }],
            yAxes: [{
              gridLines: {
                lineWidth: 1.5,
                zeroLineWidth: 0.5,
                borderDash: [8, 4],
                drawOnChartArea: true,
                tickMarkLength: 0,
                drawBorder: false,
                // borderColor: this.getLineGradient,
              },
              ticks: {
                display: true,
                maxTicksLimit: 1,
                min: this.minY,
                max: this.maxY,
                fontFamily: 'Montserrat',
                padding: 10,
              }
            }]
          } ,
          tooltips: {
            enabled: true,
            intersect: false,
            mode: "index",
            backgroundColor: '#6b70ed',
            titleFontFamily: 'Montserrat',
            bodyFontFamily: 'Montserrat',
            displayColors: false,
            callbacks: {
              label: function(tooltipItem, data) {
                var label = data.datasets[tooltipItem.datasetIndex].label || '';

                if (label) {
                  label += ': ';
                }
                label += (Math.round(tooltipItem.yLabel * 100) / 100).toLocaleString('en-US');
                return '$' + label;
              }
            }
          }
        }
      }
    },
    watch: {
      dataPoints() {
        this.renderChart(this.chartData, this.options);
      },
      options() {
        this.renderChart(this.chartData, this.options);
      }
    },
    methods: {
      getLineGradient(ctx, chartArea) {
        let gradient = ctx.createLinearGradient(0, chartArea.bottom, 0, chartArea.top);
        gradient.addColorStop(0, '#fa91bf');
        gradient.addColorStop(1, '#babafe');

        return gradient;
      },
      getColor(context, fun) {
        let chart = context.chart;
        let {ctx, chartArea} = chart;

        if (!chartArea) {
          // This case happens on initial chart load
          return null;
        }

        return fun(ctx, chartArea);
      }
    }
  }
</script>

